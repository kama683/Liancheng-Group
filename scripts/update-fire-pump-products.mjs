const SECTION_ORDER = [
  "xbd-d",
  "xbd-dv",
  "xbd-dw",
  "xbd-w",
  "xbd-slw2",
  "xbd-sls2",
  "xbd-slow",
  "xbd-gdl",
  "xbc",
];

const sectionProducts = SECTION_ORDER.map((slug) => {
  const product = FIRE_PRODUCTS[slug];
  return { code: product.code, name: product.name, slug };
});

for (const [slug, product] of Object.entries(FIRE_PRODUCTS)) {
  const existing = data.products[slug];
  data.products[slug] = {
    ...(existing ?? {}),
    code: product.code,
    name: product.name,
    slug,
    section: SECTION,
    category: "pumps",
    description: product.description,
    specs: product.specs,
    applications: product.applications,
    image: product.image,
  };
}

const migrated = new Set(SECTION_ORDER);
for (const [slug, product] of Object.entries(data.products)) {
  if (product.section !== SECTION || migrated.has(slug)) continue;
  product.section = PARKED_SECTION;
}

const fireSection = data.sections.find((s) => s.title === SECTION);
if (fireSection) {
  fireSection.products = sectionProducts;
}

fs.writeFileSync(catalogPath, JSON.stringify(data, null, 2) + "\n", "utf8");
console.log(
  "Updated fire pump products:",
  sectionProducts.map((p) => p.slug).join(", ")
);

const sectionProducts = [
  "yw",
  "wqx",
  "wq-ii",
  "wl",
  "wqc",
  "wq",
].map((slug) => {
  const product = WASTEWATER_PRODUCTS[slug];
  return { code: product.code, name: product.name, slug };
});

for (const [slug, product] of Object.entries(WASTEWATER_PRODUCTS)) {
  const existing = data.products[slug];
  data.products[slug] = {
    ...(existing ?? {}),
    code: product.code,
    name: product.name,
    slug,
    section: SECTION,
    category: "pumps",
    description: product.description,
    specs: product.specs,
    applications: product.applications,
    image: product.image,
  };
}

const wastewaterSection = data.sections.find((s) => s.title === SECTION);
if (wastewaterSection) {
  wastewaterSection.products = sectionProducts;
}

fs.writeFileSync(catalogPath, JSON.stringify(data, null, 2) + "\n", "utf8");

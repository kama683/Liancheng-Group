const SECTION_ORDER = ["smart-pump-station", "smart-pump-room", "zwlv"];

const sectionProducts = SECTION_ORDER.map((slug) => {
  const product = WATER_SUPPLY_PRODUCTS[slug];
  return { code: product.code, name: product.name, slug };
});

for (const [slug, product] of Object.entries(WATER_SUPPLY_PRODUCTS)) {
  const existing = data.products[slug];
  data.products[slug] = {
    ...(existing ?? {}),
    code: product.code,
    name: product.name,
    slug,
    section: SECTION,
    category: "pumps",
    description: product.description,
    specs: product.specs,
    applications: product.applications,
    image: product.image,
  };
}

const migrated = new Set(SECTION_ORDER);
for (const [slug, product] of Object.entries(data.products)) {
  if (product.section !== SECTION || migrated.has(slug)) continue;
  product.section = PARKED_SECTION;
}

const waterSection = data.sections.find((s) => s.title === SECTION);
if (waterSection) {
  waterSection.products = sectionProducts;
  waterSection.paragraphs = [
    "Модульные решения для водоснабжения — комплексные насосные станции и системы подачи воды с интеллектуальным управлением, энергосбережением и возможностью удаленного мониторинга.",
  ];
}


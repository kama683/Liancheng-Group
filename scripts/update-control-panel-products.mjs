const SECTION_ORDER = [
  "lec",
  "lbp",
  "hv-control-cabinet",
  "lfb",
  "lv-distribution",
];

const sectionProducts = SECTION_ORDER.map((slug) => {
  const product = CONTROL_PRODUCTS[slug];
  return { code: product.code, name: product.name, slug };
});

for (const [slug, product] of Object.entries(CONTROL_PRODUCTS)) {
  const existing = data.products[slug];
  data.products[slug] = {
    ...(existing ?? {}),
    code: product.code,
    name: product.name,
    slug,
    section: SECTION,
    category: "pumps",
    description: product.description,
    specs: product.specs,
    applications: product.applications,
    image: product.image,
  };
}

const controlSection = data.sections.find((s) => s.title === SECTION);
if (controlSection) {
  controlSection.products = sectionProducts;
  controlSection.paragraphs = SECTION_PARAGRAPHS;
}

fs.writeFileSync(catalogPath, JSON.stringify(data, null, 2) + "\n", "utf8");
console.log(
  "Updated control panel products:",
  sectionProducts.map((p) => p.slug).join(", ")
);
